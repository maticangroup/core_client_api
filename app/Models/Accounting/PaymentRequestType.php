<?php

namespace Matican\Models\Accounting;



use Matican\Core\Concepts\CRUDActions;
use Matican\Core\DataTypes\MachineName;

class PaymentRequestType extends CRUDActions
{
    /**
     * @param integer
     */
    private $id;

    /**
     * @param string
     */
    private $name;

    /**
     * @param MachineName
     */
    private $machine_name;

    /**
     * @param PaymentRequest
     */
    private $payment_requests;

    public function __construct()
    {
        $this->payment_requests = [];
    }

    /**
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return PaymentRequestType
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return MachineName
     */
    public function getMachineName()
    {
        return $this->machine_name;
    }

    /**
     * @param MachineName $machine_name
     * @return PaymentRequestType
     */
    public function setMachineName(MachineName $machine_name)
    {
        $this->machine_name = $machine_name;

        return $this;
    }

    /**
     * @return PaymentRequest[]
     */
    public function getPaymentRequests()
    {
        return $this->payment_requests;
    }
}
