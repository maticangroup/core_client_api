<?php

namespace Matican\Models\CRM;


use Matican\Core\Concepts\CRUDActions;
use Matican\Core\DataTypes\MachineName;

class CustomerGroupStatus extends CRUDActions
{
    /**
     * @param integer
     */
    private $id;

    /**
     * @param string
     */
    private $name;

    /**
     * @param MachineName
     */
    private $machine_name;

    /**
     * @param CustomerGroup
     */
    private $groups;

    public function __construct()
    {
        $this->groups = [];
    }

    /**
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return CustomerGroupStatus
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return MachineName
     */
    public function getMachineName()
    {
        return $this->machine_name;
    }

    /**
     * @param MachineName $machine_name
     * @return CustomerGroupStatus
     */
    public function setMachineName(MachineName $machine_name)
    {
        $this->machine_name = $machine_name;

        return $this;
    }

    /**
     * @return CustomerGroup[]
     */
    public function getGroups()
    {
        return $this->groups;
    }
}
